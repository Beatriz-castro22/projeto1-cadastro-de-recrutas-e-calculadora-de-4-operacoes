let num1 = parseInt(prompt("Digite o primeiro numero"))
let num2 = parseInt(prompt("Digite o segundo numero"))
let soma = num1 + num2
let sub = num1 - num2
let mult = num1 * num2
let div = num1 / num2

window.alert(`resultado:
${num1} + ${num2} = ${soma}
${num1} - ${num2} = ${sub}
${num1} * ${num2} = ${mult}
${num1} / ${num2} = ${div}`)